#!/usr/bin/env node
/* gen-icomoon-data.mjs — merge the IcoMoon selection.json files into the ODC window-global.
 *
 * Companion to gen-icomoon.mjs. The Live Style Guide's <loop-icon-reference> renders each
 * tile as an inline SVG straight from the IcoMoon path data. In the local preview it can
 * fetch() the selection.json files directly; ODC blocks fetch/CORS, so — exactly like
 * loop-icon-data.js — it consumes a plain <script> Resource that sets a window global.
 *
 * This reads the THREE per-style selection.json in dist/icomoon/ (NOT icons.json) and merges
 * them, keyed by icon name, into window.LoopIcoMoonData. Reading the selection.json (rather
 * than regenerating from icons.json) is what makes the MAINTENANCE ROUND-TRIP work: a
 * developer who edits the set in IcoMoon and drops their re-exported selection.json back into
 * dist/icomoon/ can re-run THIS script to refresh the component data, without gen:icomoon
 * clobbering their IcoMoon edits from icons.json.
 *
 * Input:  dist/icomoon/loop-fa-{solid,regular,light}.selection.json  (from gen:icomoon, or an
 *         IcoMoon re-export dropped in the same place)
 * Output: dist/icomoon/loop-icomoon-data.js  — gitignored (carries LICENSED FA Pro paths;
 *         never committed). Paste into ODC as a <script> Resource BEFORE loop-icon-reference.js.
 *
 * Global shape (compact; one icon per line for readable diffs):
 *   window.LoopIcoMoonData = { height: 512, icons: [
 *     { n:"user", tags:[...], s:{c:61447,w:448,p:"M224 256A128..."}, r:{...}, l:{...} }, ... ] }
 *   where each style key (s/r/l) carries { c:codepoint, w:width, p:path }. A missing style key
 *   just means the icon doesn't ship that style.
 *
 * --lean omits the `p` (path) field → a lightweight global; the component then falls back to
 * font rendering in ODC. Use it if the full ~8-12 MB Resource is too heavy.
 *
 * License: Font Awesome Pro (Commercial) — do NOT redistribute; output stays in dist/.
 *
 * Usage: node build/gen-icomoon-data.mjs [--lean]   (npm run gen:icomoon-data) */
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const icomoonDir = join(root, "dist", "icomoon");
const outFile = join(icomoonDir, "loop-icomoon-data.js");

const LEAN = process.argv.includes("--lean");

/* filename → style flag used in the merged global */
const FILES = [
  { flag: "s", file: "loop-fa-solid.selection.json" },
  { flag: "r", file: "loop-fa-regular.selection.json" },
  { flag: "l", file: "loop-fa-light.selection.json" },
];

const missing = FILES.filter((f) => !existsSync(join(icomoonDir, f.file)));
if (missing.length) {
  console.error(
    `missing ${missing.map((m) => m.file).join(", ")} in dist/icomoon/\n` +
      "Run npm run gen:icomoon first (or drop your IcoMoon-exported selection.json there)."
  );
  process.exit(1);
}

// Merge, keyed by icon name, preserving manifest order from the first file that introduces a name.
const byName = new Map();
let height = 512;

for (const { flag, file } of FILES) {
  const doc = JSON.parse(readFileSync(join(icomoonDir, file), "utf8"));
  if (typeof doc.height === "number") height = doc.height;
  for (const entry of doc.icons || []) {
    const name = entry.properties && entry.properties.name;
    if (!name) continue;
    let rec = byName.get(name);
    if (!rec) {
      rec = { n: name, tags: entry.icon.tags || [name] };
      byName.set(name, rec);
    }
    const style = { c: entry.properties.code, w: entry.icon.width };
    if (!LEAN) style.p = (entry.icon.paths && entry.icon.paths[0]) || "";
    rec[flag] = style;
  }
}

const icons = [...byName.values()];
const styleCount = FILES.reduce((a, f) => ((a[f.flag] = icons.filter((i) => i[f.flag]).length), a), {});

const banner = [
  "/* loop-icomoon-data.js — GENERATED by build/gen-icomoon-data.mjs. Do not edit by hand.",
  " *",
  ` * Merged Font Awesome 6 Pro icon data for <loop-icon-reference>, sourced from the IcoMoon`,
  ` * selection.json files in dist/icomoon/. ${icons.length} icons`,
  ` * (${styleCount.s} solid · ${styleCount.r} regular · ${styleCount.l} light).${LEAN ? " LEAN build — no paths (font render)." : ""}`,
  " *",
  " * Row shape: { n:name, tags:[...], s?:{c:code,w:width" + (LEAN ? "" : ",p:path") + "}, r?:{...}, l?:{...} }",
  " *",
  " * Loaded in ODC as a <script> Resource BEFORE loop-icon-reference.js — sets the",
  " * window.LoopIcoMoonData global the component reads. Regenerate: npm run gen:icomoon-data.",
  " * LICENSED Font Awesome Pro artwork — do NOT redistribute (this file is gitignored).",
  " */",
].join("\n");

// One icon per line keeps the (large) file diff-friendly.
const rows = icons.map((r) => "  " + JSON.stringify(r)).join(",\n");
const body = `${banner}\nwindow.LoopIcoMoonData = { height: ${height}, icons: [\n${rows}\n] };\n`;
writeFileSync(outFile, body);
console.log(
  `gen:icomoon-data → dist/icomoon/loop-icomoon-data.js (${icons.length} icons${LEAN ? ", lean/no-paths" : ""}, ${Math.round(body.length / 1024)}KB)`
);

#!/usr/bin/env node
/* gen-type-utilities.mjs — generates tokens/typography-utilities.css from
 * tokens/typography.css.
 *
 * Parallels gen-color-utilities.mjs. Emits single-purpose font-size + font-weight
 * helpers in the OutSystems UI mould (vendor 05-useful/_typography.scss):
 *   .font-size-<name>   { font-size:  var(--font-size-<name>) !important; }
 *   .font-weight-<name> { font-weight: var(--font-weight-<name>) !important; }
 *
 * Token-only (hard rule #3): every value resolves through the existing scale token, so
 * a re-map flows through automatically. `!important` matches color-utilities.css
 * (opt-in single-declaration overrides attached via ExtendedClass, hard rule #7).
 *
 * Scope = the raw SCALE + WEIGHT primitives only. The NAMED type styles (Heading H1
 * Large, Body Large, …) combine size+weight+line-height+tracking and are documented
 * live by <loop-type-reference>; some of their tracking values are off the token scale
 * (see findings register) so they are intentionally NOT minted as classes here.
 *
 * Usage:  node build/gen-type-utilities.mjs   (or: npm run gen:type-utilities)
 * Output: tokens/typography-utilities.css     (then run npm run build:theme) */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const srcFile = join(root, "tokens", "typography.css");
const outFile = join(root, "tokens", "typography-utilities.css");

function collect(css, prefix) {
  const re = new RegExp(`^\\s*--${prefix}-([\\w-]+)\\s*:\\s*([^;]+);`, "gm");
  const out = [];
  let m;
  while ((m = re.exec(css))) out.push({ name: m[1], value: m[2].trim() });
  return out;
}

const css = readFileSync(srcFile, "utf8");
const sizes = collect(css, "font-size");
const weights = collect(css, "font-weight");

function block(title, tokens, prop, varPrefix, clsPrefix) {
  const banner = `/* ===================== ${title} ===================== */`;
  const rules = tokens
    .map((t) => `.${clsPrefix}-${t.name} { ${prop}: var(--${varPrefix}-${t.name}) !important; }  /* ${t.value} */`)
    .join("\n");
  return `${banner}\n${rules}`;
}

const head = `/* typography-utilities.css — GENERATED by build/gen-type-utilities.mjs.
 * DO NOT EDIT BY HAND — rerun \`npm run gen:type-utilities\` after changing typography.css.
 *
 * Single-purpose .font-size-<name> / .font-weight-<name> helpers for the type scale +
 * weights, in the OutSystems UI mould. Attach via ExtendedClass (hard rule #7); values
 * resolve through var(--font-size-*) / var(--font-weight-*) (hard rule #3). \`!important\`
 * matches color-utilities.css (opt-in single-declaration overrides).
 *
 * ${sizes.length} sizes + ${weights.length} weights = ${sizes.length + weights.length} utilities.
 * Rendered live in the Live Style Guide by <loop-type-reference>
 * (src/components/loop-type-reference.js), which also documents the named Figma type
 * styles (Heading/Body roles). */\n`;

const out = `${head}\n${block("Font size — scale", sizes, "font-size", "font-size", "font-size")}\n\n${block(
  "Font weight",
  weights,
  "font-weight",
  "font-weight",
  "font-weight"
)}\n`;

writeFileSync(outFile, out);
console.log(
  `gen:type-utilities → tokens/typography-utilities.css (${sizes.length} sizes, ${weights.length} weights, ${sizes.length + weights.length} utilities)`
);

#!/usr/bin/env node
/**
 * build-osui.mjs — compile the REAL OutSystems UI framework CSS for the local preview.
 *
 * The preview's job is to validate fidelity against the SAME base ODC ships, not a
 * hand-written approximation. This compiles the vendored OutSystems UI SCSS (the
 * `vendor/outsystems-ui` submodule) into a single stylesheet the preview loads as
 * layer 1 — exactly the framework base that sits beneath the Theme CSS in ODC.
 *
 * Load order in preview/index.html mirrors ODC:
 *   1) THIS file (real OutSystems UI base)        ← framework
 *   2) dist/tokens.css + dist/theme.css            ← brand tokens + classes (retints OSUI vars)
 *   3) provider CSS (virtual-select, flatpickr)    ← runtime-injected providers
 *   4) src/blocks/loop-*.css                        ← Theme CSS overrides (must win)
 *
 * Platform: ODC (per CLAUDE.md). Switch ENTRY to the O11 file for an O11 project.
 *
 * Re-run after updating the submodule:  npm run build:osui
 */
import * as sass from 'sass';
import { writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const ENTRY = resolve(root, 'vendor/outsystems-ui/src/scss/ODC.OutSystemsUI.scss');
const OUT = resolve(root, 'preview/vendor/outsystems-ui/outsystems-ui.css');

const banner =
	`/* GENERATED by build/build-osui.mjs — real OutSystems UI (ODC) framework base.\n` +
	`   Source: vendor/outsystems-ui (submodule). Do not edit by hand; run \`npm run build:osui\`.\n` +
	`   This is the framework layer the preview loads BENEATH dist/tokens.css + dist/theme.css + loop-*.css. */\n`;

const { css } = sass.compile(ENTRY, { style: 'expanded', sourceMap: false, quietDeps: true });

// Drop Sass's leading `@charset "UTF-8";` — it must be the first byte to be valid, but
// our banner precedes it. The preview declares <meta charset="utf-8">, so it's redundant.
const body = css.replace(/^@charset "UTF-8";\s*/i, '');

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, banner + body);

console.log(`build:osui → ${OUT.replace(root + '\\', '').replace(root + '/', '')}  (${(banner.length + css.length) / 1024 | 0} KB)`);

#!/usr/bin/env node
/* gen-color-utilities.mjs — generates tokens/color-utilities-primitives.css from
 * tokens/colors.css.
 *
 * Mirrors the OutSystems UI pattern (vendor 05-useful/_colors-*.scss): for every
 * primitive color token `--color-<name>` it emits a `.background-<name>` and a
 * `.text-<name>` single-declaration utility, each pointing at the token via
 * `var(--token)` (hard rule #3 — never hard-code a value). `!important` matches the
 * convention in color-utilities.css: utilities are opt-in, attached via ExtendedClass
 * (hard rule #7), and must win over OS UI's own color rules.
 *
 * Why generate? The primitive ramps are large and mechanical — hand-maintaining ~200
 * one-liners drifts from colors.css. This script is the single source: rerun it
 * whenever a primitive token changes.
 *
 * Sections follow the `---- Title ----` group comments in colors.css, so the output
 * reads in the same order as the primitives and matches the Live Style Guide grouping
 * rendered by <loop-color-reference>.
 *
 * Usage:  node build/gen-color-utilities.mjs   (or: npm run gen:color-utilities)
 * Output: tokens/color-utilities-primitives.css  (then run npm run build:theme) */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const srcFile = join(root, "tokens", "colors.css");
const outFile = join(root, "tokens", "color-utilities-primitives.css");

/* Parse colors.css into ordered groups. A `---- Title ----` comment opens a group;
 * each `--color-<name>: <value>;` line inside adds a token to the current group. */
function parse(css) {
  const groups = [];
  let current = null;
  const GROUP_RE = /\/\*\s*-+\s*(.+?)\s*-+\s*\*\//;
  const VAR_RE = /^\s*--color-([\w-]+)\s*:\s*([^;]+);/;
  for (const line of css.split(/\r?\n/)) {
    const g = line.match(GROUP_RE);
    if (g) {
      current = { title: g[1].trim(), tokens: [] };
      groups.push(current);
      continue;
    }
    const v = line.match(VAR_RE);
    if (v && current) current.tokens.push({ name: v[1], value: v[2].trim() });
  }
  return groups.filter((grp) => grp.tokens.length);
}

function emit(groups) {
  const count = groups.reduce((n, g) => n + g.tokens.length, 0);
  const head = `/* color-utilities-primitives.css — GENERATED by build/gen-color-utilities.mjs.
 * DO NOT EDIT BY HAND — rerun \`npm run gen:color-utilities\` after changing colors.css.
 *
 * Single-purpose .background-<name> / .text-<name> helpers for every PRIMITIVE color
 * token, in the OutSystems UI mould. Attach via ExtendedClass (hard rule #7). Values
 * resolve through var(--color-<name>) so a token re-map flows through automatically
 * (hard rule #3). \`!important\` matches color-utilities.css (opt-in single-declaration
 * overrides that must beat OS UI's own color classes).
 *
 * These are the raw PRIMITIVES (the ramps). For brand/semantic ROLE helpers
 * (.background-primary, .text-error, .loop-text-default, …) see color-utilities.css.
 *
 * ${count} tokens → ${count * 2} utilities. Rendered live in the Live Style Guide by
 * the <loop-color-reference> Web Component (src/components/loop-color-reference.js). */\n`;

  const blocks = groups.map((g) => {
    const banner = `/* ===================== ${g.title} ===================== */`;
    const rules = g.tokens
      .map((t) => {
        const cls = t.name;
        const bg = `.background-${cls} { background-color: var(--color-${cls}) !important; }`;
        const tx = `.text-${cls}       { color: var(--color-${cls}) !important; }`;
        return `${bg}\n${tx}`;
      })
      .join("\n");
    return `${banner}\n${rules}`;
  });

  return `${head}\n${blocks.join("\n\n")}\n`;
}

const groups = parse(readFileSync(srcFile, "utf8"));
writeFileSync(outFile, emit(groups));
const count = groups.reduce((n, g) => n + g.tokens.length, 0);
console.log(
  `gen:color-utilities → tokens/color-utilities-primitives.css (${groups.length} groups, ${count} tokens, ${count * 2} utilities)`
);

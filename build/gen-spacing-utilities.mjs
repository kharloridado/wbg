#!/usr/bin/env node
/* gen-spacing-utilities.mjs — generates tokens/spacing-utilities.css from
 * tokens/spacing.css.
 *
 * Directional margin / padding utility classes in the EXACT OutSystems UI mould
 * (vendor 05-useful/_space-margin.scss + _space-padding.scss). For every step of the
 * descriptive spacing scale `--space-<name>` it emits:
 *
 *   .margin-<name>          { margin:        var(--space-<name>); }   // all sides
 *   .margin-top-<name>      { margin-top:    var(--space-<name>); }   // one side …
 *   .margin-right-<name>    { margin-right:  var(--space-<name>); }
 *   .margin-bottom-<name>   { margin-bottom: var(--space-<name>); }
 *   .margin-left-<name>     { margin-left:   var(--space-<name>); }
 *   .margin-x-<name>        { margin-inline: var(--space-<name>); }   // left + right
 *   .margin-y-<name>        { margin-block:  var(--space-<name>); }   // top + bottom
 *
 * …and the identical set for `.padding-*`, plus `.margin-auto` ( 0 auto ). An `.is-rtl`
 * block flips the side-specific left/right classes, mirroring OS UI's RTL handling.
 *
 * This is the SAME naming OutSystems UI ships — only the size keys differ: OS UI uses
 * none/xs/s/base/m/l/xl/xxl (whose px values already match The Loop scale), while The
 * Loop exposes the full descriptive scale from Figma (Spacing System node 10994-5086):
 * none/xtiny/tiny/xxsmall/xsmall/small/regular/medium/large/xlarge/xxlarge/huge/xhuge.
 * Generating Loop-named directional classes lets developers use the Style-Guide names
 * directly (e.g. `.padding-x-regular`, `.margin-top-small`) via ExtendedClass.
 *
 * Token-only (hard rule #3): every value resolves through var(--space-<name>) so a
 * re-map flows through automatically. NO `!important` — matches OS UI's own space
 * utilities (layout helpers applied via ExtendedClass on containers, not overrides).
 * Scope = the descriptive scale ONLY (lowercase-letter names); numeric aliases
 * (--space-100/200/500) and component gaps (--space-*-gap, group) are intentionally
 * excluded — they are internal, not layout utilities.
 *
 * Usage:  node build/gen-spacing-utilities.mjs   (or: npm run gen:spacing-utilities)
 * Output: tokens/spacing-utilities.css           (then run npm run build:theme) */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const srcFile = join(root, "tokens", "spacing.css");
const outFile = join(root, "tokens", "spacing-utilities.css");

/* Collect --space-<name> tokens, keeping only the descriptive scale: names made of
 * lowercase letters (none, xtiny, … xhuge). Excludes numeric aliases (--space-100) and
 * component gaps (--space-button-gap, --space-checkbox-group-row). Order = source order. */
const css = readFileSync(srcFile, "utf8");
const scale = [];
const re = /^\s*--space-([a-z]+)\s*:\s*([^;]+);/gm;
let m;
while ((m = re.exec(css))) scale.push({ name: m[1], value: m[2].trim() });

const SIDES = ["top", "right", "bottom", "left"];

/* One property family (margin | padding) for one scale step. */
function familyBlock(prop, t) {
  const v = `var(--space-${t.name})`;
  const lines = [
    `.${prop}-${t.name} { ${prop}: ${v}; }`,
    ...SIDES.map((side) => `.${prop}-${side}-${t.name} { ${prop}-${side}: ${v}; }`),
    `.${prop}-x-${t.name} { ${prop}-inline: ${v}; }`,
    `.${prop}-y-${t.name} { ${prop}-block: ${v}; }`,
  ];
  return lines.join("\n");
}

function block(prop) {
  const banner = `/* ===================== ${prop[0].toUpperCase() + prop.slice(1)} — directional ===================== */`;
  const steps = scale.map((t) => `/* ${t.name} · ${t.value} */\n${familyBlock(prop, t)}`).join("\n\n");
  return `${banner}\n${steps}`;
}

/* RTL: flip side-specific left/right so `-left-` reads as the inline-start edge. The
 * logical `-inline`/`-block` shorthands above are already direction-aware, so only the
 * physical -left-/-right- helpers need an override. */
function rtlBlock() {
  const rows = scale
    .map((t) => {
      const v = `var(--space-${t.name})`;
      return [
        `.is-rtl .margin-right-${t.name} { margin-right: var(--space-none); margin-left: ${v}; }`,
        `.is-rtl .margin-left-${t.name} { margin-left: var(--space-none); margin-right: ${v}; }`,
        `.is-rtl .padding-right-${t.name} { padding-right: var(--space-none); padding-left: ${v}; }`,
        `.is-rtl .padding-left-${t.name} { padding-left: var(--space-none); padding-right: ${v}; }`,
      ].join("\n");
    })
    .join("\n");
  return `/* ===================== RTL — flip physical left/right ===================== */\n${rows}`;
}

const head = `/* spacing-utilities.css — GENERATED by build/gen-spacing-utilities.mjs.
 * DO NOT EDIT BY HAND — rerun \`npm run gen:spacing-utilities\` after changing spacing.css.
 *
 * Directional margin / padding helpers in the OutSystems UI mould (_space-margin.scss /
 * _space-padding.scss): .margin-<name>, .margin-{top|right|bottom|left}-<name>,
 * .margin-x-<name> (inline), .margin-y-<name> (block), .margin-auto — and the identical
 * .padding-* set. Attach via ExtendedClass (hard rule #7); values resolve through
 * var(--space-<name>) (hard rule #3). No \`!important\` — matches OS UI's own space
 * utilities. Scope = the descriptive scale only.
 *
 * ${scale.length} scale steps → ${scale.length * 7 * 2 + 1} utilities (+ RTL flips).
 * Rendered live in the Live Style Guide by <loop-spacing-reference>
 * (src/components/loop-spacing-reference.js). */\n`;

const marginAuto = `/* center horizontally */\n.margin-auto { margin: 0 auto; }`;

const out = `${head}\n${block("margin")}\n\n${marginAuto}\n\n${block("padding")}\n\n${rtlBlock()}\n`;

writeFileSync(outFile, out);
console.log(
  `gen:spacing-utilities → tokens/spacing-utilities.css (${scale.length} steps, ${scale.length * 7 * 2 + 1} utilities + RTL flips)`
);

#!/usr/bin/env node
/* gen-icon-data.mjs — emit src/components/loop-icon-data.js from the vendored icon manifest.
 *
 * The Live Style Guide icon reference (<loop-icon-reference>) needs the full Font Awesome 6
 * Pro icon list — names, labels, which styles each supports, and search terms. ODC loads
 * this as a plain <script> Resource (a window global, NO fetch/CORS), so we wrap the JSON
 * manifest into `window.LoopIconData = [...]`.
 *
 * Source of truth: vendor/fontawesome-6/icon-manifest.json — a slim derivative of Font
 * Awesome's own metadata/icon-families.json (FA Pro 6.7.2 desktop package). Each row:
 *   { n: name, l: label, s: styleFlags ("s"=solid "r"=regular "l"=light),
 *     t?: [searchTerms] }
 *
 * Optional refresh: if the FULL FA metadata is dropped in at
 * vendor/fontawesome-6/metadata/icon-families.json (not committed — 110 MB in the Pro
 * package), this script REGENERATES the slim manifest from it first, then emits the data
 * file. Normally the committed manifest is used as-is.
 *
 * Usage: node build/gen-icon-data.mjs */
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const vendorDir = join(root, "vendor", "fontawesome-6");
const manifestFile = join(vendorDir, "icon-manifest.json");
const fullMeta = join(vendorDir, "metadata", "icon-families.json");
const outFile = join(root, "src", "components", "loop-icon-data.js");

const STYLE = { solid: "s", regular: "r", light: "l" };

/* Rebuild the slim manifest from FA's full metadata (only when that file is present). */
function regenManifestFromFullMeta() {
  const j = JSON.parse(readFileSync(fullMeta, "utf8"));
  const out = [];
  for (const name of Object.keys(j).sort()) {
    const e = j[name];
    const lic = e.familyStylesByLicense || {};
    // Pro metadata when available (solid/regular/light), Free fallback; classic family
    // only (sharp / duotone / thin are not shipped, and brands are excluded — no brands font).
    const fam = (lic.pro && lic.pro.length ? lic.pro : lic.free) || [];
    const styles = [...new Set(fam.filter((x) => x.family === "classic").map((x) => STYLE[x.style]).filter(Boolean))].join("");
    if (!styles) continue;
    let terms = ((e.search && e.search.terms) || []).map((t) => String(t).toLowerCase());
    terms = [...new Set(terms)].filter((t) => t && !name.includes(t));
    const row = { n: name, l: e.label || name, s: styles, u: e.unicode };
    if (terms.length) row.t = terms;
    out.push(row);
  }
  writeFileSync(manifestFile, JSON.stringify(out));
  console.log(`gen:icon-data → regenerated icon-manifest.json from full metadata (${out.length} icons)`);
  return out;
}

function build() {
  let manifest;
  if (existsSync(fullMeta)) {
    manifest = regenManifestFromFullMeta();
  } else {
    if (!existsSync(manifestFile)) throw new Error(`missing ${manifestFile} (and no full metadata to regenerate it)`);
    manifest = JSON.parse(readFileSync(manifestFile, "utf8"));
  }

  const counts = manifest.reduce(
    (a, r) => ({
      s: a.s + (r.s.includes("s") ? 1 : 0),
      r: a.r + (r.s.includes("r") ? 1 : 0),
      l: a.l + (r.s.includes("l") ? 1 : 0),
    }),
    { s: 0, r: 0, l: 0 }
  );
  const tiles = counts.s + counts.r + counts.l;

  const banner = [
    "/* loop-icon-data.js — GENERATED by build/gen-icon-data.mjs. Do not edit by hand.",
    " *",
    ` * The complete Font Awesome 6 Pro icon list for <loop-icon-reference> (the Live Style`,
    ` * Guide searchable icon grid). ${manifest.length} icons → ${tiles} per-style tiles`,
    ` * (${counts.s} solid · ${counts.r} regular · ${counts.l} light — brands not shipped).`,
    " *",
    " * Row shape: { n: name, l: label, s: styleFlags, t?: [searchTerms] }",
    ` *   styleFlags: "s"=solid, "r"=regular, "l"=light (a name can support several).`,
    " *",
    " * Loaded in ODC as a <script> Resource BEFORE loop-icon-reference.js — it sets the",
    " * window.LoopIconData global the component reads. Regenerate: npm run gen:icon-data.",
    " */",
  ].join("\n");

  // One icon per line keeps the (large) file diff-friendly.
  const rows = manifest.map((r) => "  " + JSON.stringify(r)).join(",\n");
  const body = `${banner}\nwindow.LoopIconData = [\n${rows}\n];\n`;
  writeFileSync(outFile, body);
  console.log(`gen:icon-data → src/components/loop-icon-data.js (${manifest.length} icons, ${tiles} tiles, ${Math.round(body.length / 1024)}KB)`);
}

build();
